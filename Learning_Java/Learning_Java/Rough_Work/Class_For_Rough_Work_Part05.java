package com.Learning_Java.Rough_Work;

public class Class_For_Rough_Work_Part05 {

  public static void main(String[] args) {
    int a = 1, b = 0;
    System.out.println((a == b) ? true + " a is equal to b" : false + " a is not equal to b");
    System.out.println(
        (a < b) ? "a is less than b" : (a == b) ? "a is equal to b" : "a is greater than b");
  }
}
