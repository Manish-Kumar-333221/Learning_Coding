public class Class_For_Rough_Work_Part20 {

    public static void main(String[] args) {

        int a = 5;

        System.out.println(
                (Math.pow((1 + Math.sqrt(5)), a))
                        - (Math.pow((1 - Math.sqrt(5)), a)) / (Math.pow(2, a) * Math.sqrt(5)));
    }
}
